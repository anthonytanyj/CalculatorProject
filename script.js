function addition(a,b){
    a_int = parseInt(a);
    b_int = parseInt(b);

    return a_int + b_int
}

function subtract(a,b){
    a_int = parseInt(a);
    b_int = parseInt(b);

    return a_int - b_int
}

function multiply(a,b){
    a_int = parseInt(a);
    b_int = parseInt(b);

    return a_int * b_int
}

function divide(a,b){
    a_int = parseInt(a);
    b_int = parseInt(b);

    return a_int / b_int
}
